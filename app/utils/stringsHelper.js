/**
 * Check if inputUrl is an aboluteUrl
 * return true if inputUrl is an absolute url, false otherwise
 * 
 * @param {String} inputUrl 
 */
function isAbsoluteUrl(inputUrl) {
    let absoluteUrlRegExp = new RegExp('^(?:[a-z]+:)?//', 'i');
    return absoluteUrlRegExp.test(inputUrl);
}

/**
 * function to slugify a text
 * @param {String} text => the slugified text
 */
function slugifyText(text) {
    return text.toString().toLowerCase()
        .replace(/\s+/g, '-') // Replace spaces with -
        .replace(/[^\w\-]+/g, '') // Remove all non-word chars
        .replace(/\-\-+/g, '-') // Replace multiple - with single -
        .replace(/^-+/, '') // Trim - from start of text
        .replace(/-+$/, '') // Trim - from end of text
        .replace(/-/g, '_'); // Replace remaining '-' with '_'
}

function slugifyFileName(filename) {
    let extension = path.extname(filename);
    let basename = path.basename(filename, extension);

    let slugifiedFileName = basename.toString().toLowerCase()
        .replace(/\s+/g, '-') // Replace spaces with -
        .replace(/[^\w\-]+/g, '') // Remove all non-word chars
        .replace(/\-\-+/g, '-') // Replace multiple - with single -
        .replace(/^-+/, '') // Trim - from start of text
        .replace(/-+$/, '') // Trim - from end of text
        .replace(/-/g, '_'); // Replace remaining '-' with '_'

    return slugifiedFileName + '_' + uuidv4() + extension;
}

function isJsonString(str) {
    try {
        JSON.parse(str);
    } catch (e) {
        return false;
    }
    return true;
}

function parseJsonString(str) {
    let parsedStr = '[]';
    try {
        parsedStr = JSON.parse(str);
    } catch (e) {
        return '[]';
    }
    return parsedStr;
}

module.exports = {
    isUrlValid: isUrlValid,
    isAbsoluteUrl: isAbsoluteUrl,
    slugifyText: slugifyText,
    slugifyFileName: slugifyFileName,
    isJsonString: isJsonString,
    parseJsonString, parseJsonString
};
